function iconplus() {
  const showplusEl = document.querySelector("#icon-plus");
  showplusEl.classList.add("plus-hide");
  const showparaEl = document.querySelector("#big-para");
  showparaEl.classList.remove("big-para");
  const showminuesEl = document.querySelector("#icon-minus");
  showminuesEl.classList.remove("minus-hide");
  showminuesEl.classList.add("minus-show");
}

